# Variabel x bertipe integer
x = 10
print(x)  # Output: 10

# Mengubah tipe data variabel x menjadi string
x = "Hello"
print(x)  # Output: Hello

# Variabel y bertipe float
y = 3.14
print(y)  # Output: 3.14

# Mengubah nilai variabel y menjadi boolean
y = True
print(y)  # Output: True
