x = 5  # Ini adalah pernyataan penugasan, mengatur nilai variabel x menjadi 5
y = 3  # Pernyataan penugasan lainnya, mengatur nilai variabel y menjadi 3

# Pernyataan ekspresi, menghitung hasil penjumlahan variabel x dan y
jumlah = x + y

# Pernyataan print, menampilkan hasil penjumlahan ke layar
print("Hasil penjumlahan x dan y adalah:", jumlah)
